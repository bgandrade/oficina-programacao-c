#include <stdio.h>

int main(void)
{
    // Todo programa em C começa a executar pela função main.
    // Pense nela como a "porta de entrada" do programa.

    // printf serve para mostrar uma mensagem na tela.
    // Tudo que está entre aspas será exibido exatamente como foi escrito.
    printf("Olá, turma!\n");

    // O símbolo \n faz a quebra de linha.
    // Sem ele, o próximo texto poderia continuar na mesma linha.
    printf("Bem-vindos à oficina de programação em C.\n");

    // return 0 indica que o programa terminou sem erro.
    // Você verá essa linha em quase todos os programas simples em C.
    return 0;
}
